#include <libft.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *res;

	res = dst;
	while (*src && len > 0)
	{
		*res++ = *src++;
		len--;
	}
	while (len > 0)
	{
		*res++ = '\0';
		len--;
	}
	return (dst);
}
